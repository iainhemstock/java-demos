public class Initialization {

    public static void main(String[] args) {

        String s;

        //==========================================================================================
        // From string literal
        //==========================================================================================
        s = "string literal";

        //==========================================================================================
        // Default empty string
        //==========================================================================================
        s = new String(); // ""

        //==========================================================================================
        // From char array
        //==========================================================================================
        char[] chars = { 'a', 'b', 'c' };
        s = new String(chars); // "abc"

        //==========================================================================================
        // From subset of char array
        //==========================================================================================
        int offset = 0;
        int count = 2;
        s = new String(chars, offset, count); // "ab"

        //==========================================================================================
        // From byte array
        //==========================================================================================
        byte[] bytes = { 65, 66, 67 };
        s = new String(bytes); // "ABC"

        //==========================================================================================
        // From subset of byte array
        //==========================================================================================
        offset = 1;
        count = 2;
        s = new String(bytes, offset, count); // "BC"

        //==========================================================================================
        // Copy constructor
        //==========================================================================================
        String original = "original";
        String copy = new String(original);

        //==========================================================================================
        // From StringBuffer
        //==========================================================================================
        StringBuffer buffer = new StringBuffer();
        buffer.append("Hello ").append("world!");
        s = new String(buffer); // "Hello world!"

        //==========================================================================================
        // From StringBuilder
        //==========================================================================================
        StringBuilder builder = new StringBuilder();
        builder.append("Hello ");
        builder.append("world!");
        s = new String(builder); // "Hello world!"
    }
}
